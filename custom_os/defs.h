#pragma once

#define MAX_CPUS 64

#define PAGE_SIZE_BITS          12
#define PAGE_SIZE               (1 << PAGE_SIZE_BITS)
#define PAGE_SIZE_ALIGN_DOWN(x) ((x) & ~(PAGE_SIZE - 1))
#define PAGE_SIZE_ALIGN_UP(x)   ((x + PAGE_SIZE - 1) & ~(PAGE_SIZE - 1))

#define CACHE_LINE_SIZE_BYTES 64

#define TB (1ull << 40)
#define GB (1ull << 30)
#define MB (1ull << 20)
#define KB (1ull << 10)

#define KERNEL_HIGHER_HALF_START 0xffff800000000000

#define KERNEL_IMAGE_START 0xffffffff80000000

#define KERNEL_DIRECT_PHYS_MAPPING_START 0xffff888000000000
#define KERNEL_DIRECT_PHYS_MAPPING_SIZE  64 * GB

#define KERNEL_IO_MAPPING_START 0xffff640000000000
#define KERNEL_IO_MAPPING_SIZE  1 * GB

#define KERNEL_KASAN_SHADOW_MEMORY_START 0xffff960000000000
#define KERNEL_KASAN_SHADOW_MEMORY_SIZE  (KERNEL_DIRECT_PHYS_MAPPING_SIZE / 8)
#define KERNEL_KASAN_SHADOW_IMAGE_MEMORY_START 0xffffc80000000000
